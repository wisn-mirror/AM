package com.wisn.bean;

public class ResultTest {
    public String id;
    public String Name;

    public ResultTest() {
    }

    public ResultTest(String id, String name) {
        this.id = id;
        Name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }
}
